#Crea un programa que solicite al usuario dos números enteros y muestre por pantalla el resultado de las 
# siguientes operaciones: suma, resta, multiplicación y división.

num_1 = int(input("Introduce el primer numero: "))
num_2 = int(input("Introduce el segundo numero: "))
suma = num_1 + num_2
resta = num_1 - num_2
multiplicacion = num_1 * num_2
division = num_1 / num_2
print(f"La suma es {suma}, la resta es {resta}, la multiplicacion es {multiplicacion} y la division es  {division}")